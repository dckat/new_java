package org.example.day2.control.조건문if;

public class 조건문if1 {
    public static void main(String[] args) {
        int id = 111;       // 가입한 id
        int id2 = 222;      // 입력한 id

        if (id == id2) {      // 조건이 true이면
            System.out.println("아이디가 동일O");
        } else {    // 조건이 false이면
            System.out.println("아이디가 동일X");
        }
    }
}

package org.example;

import java.util.Arrays;

public class ArrPrint {

    // 배열만 출력하는 기능 --> 메소드 정의
    public static void print(int[] x) {
        System.out.println(Arrays.toString(x));
    }

    public static void print(String[] x) {
        System.out.println(Arrays.toString(x));
    }

    public static void print(double[] x) {
        System.out.println(Arrays.toString(x));
    }
}
